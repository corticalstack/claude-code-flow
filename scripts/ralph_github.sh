#!/bin/bash
# scripts/ralph_github.sh - GitHub operations for Ralph autonomous loop

# Prevent multiple sourcing
[[ -n "${RALPH_GITHUB_LOADED:-}" ]] && return 0
RALPH_GITHUB_LOADED=1

# Source utilities
_LIB_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "$_LIB_DIR/ralph_utils.sh"
