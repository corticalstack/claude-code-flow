# Fetch all open issues
gh_fetch_open_issues() {
    local limit=${1:-20}

    gh issue list --state open --limit "$limit" --json number,title,body,labels 2>/dev/null
    if [ $? -ne 0 ]; then
        log_error "Failed to fetch GitHub issues"
        return 1
    fi
}

# Check if issue is blocked
gh_is_issue_blocked() {
    local issue=$1

    # Get issue labels
    local labels=$(gh issue view "$issue" --json labels --jq '.labels[].name' 2>/dev/null)

    # Check for blocked-by-# labels
    if echo "$labels" | grep -q "^blocked-by-"; then
        local blocker=$(echo "$labels" | grep "^blocked-by-" | head -1 | sed 's/blocked-by-#//')

        # Check if blocker issue is still open
        local blocker_state=$(gh issue view "$blocker" --json state --jq '.state' 2>/dev/null)

        if [ "$blocker_state" = "OPEN" ]; then
            log_warning "Issue #$issue is blocked by #$blocker (still open)"
            return 0  # Blocked
        else
            log_info "Blocker #$blocker is resolved, issue #$issue can proceed"
            # Remove the blocked label
            gh issue edit "$issue" --remove-label "blocked-by-#$blocker" 2>/dev/null
            return 1  # Not blocked
        fi
    fi

    return 1  # Not blocked
}

# Filter out blocked issues
gh_filter_unblocked_issues() {
    local issues_json=$1

    local unblocked=()

    while read -r issue_num; do
        if ! gh_is_issue_blocked "$issue_num"; then
            unblocked+=("$issue_num")
        fi
    done < <(echo "$issues_json" | jq -r '.[].number')

    # Return filtered list as JSON
    echo "$issues_json" | jq "[.[] | select(.number as \$num | [$(IFS=,; echo "${unblocked[*]}")] | index(\$num) != null)]"
}

# Update issue label
gh_update_label() {
    local issue=$1
    local add_label=$2
    local remove_label=${3:-""}

    if [ -n "$add_label" ]; then
        gh issue edit "$issue" --add-label "$add_label" 2>/dev/null
        log_info "Added label '$add_label' to issue #$issue"
    fi

    if [ -n "$remove_label" ]; then
        gh issue edit "$issue" --remove-label "$remove_label" 2>/dev/null
        log_info "Removed label '$remove_label' from issue #$issue"
    fi
}

# Add comment to issue
gh_add_comment() {
    local issue=$1
    local comment=$2

    echo "$comment" | gh issue comment "$issue" --body-file - 2>/dev/null

    if [ $? -eq 0 ]; then
        log_info "Added comment to issue #$issue"
        return 0
    else
        log_error "Failed to add comment to issue #$issue"
        return 1
    fi
}

# Mark issue as blocked by another issue
gh_mark_blocked_by() {
    local issue=$1
    local blocker=$2
    local reason=$3

    local label="blocked-by-#$blocker"
    gh_update_label "$issue" "$label" ""

    local comment=$(cat <<EOF
## ⚠️ Blocked by Dependency

This issue is blocked by Issue #$blocker.

**Reason**: $reason

Ralph will retry this issue after #$blocker is complete.

---
🤖 Generated by Ralph Wiggum autonomous loop
EOF
)

    gh_add_comment "$issue" "$comment"
}

