# Check if issue has label
gh_has_label() {
    local issue=$1
    local label=$2

    local labels=$(gh issue view "$issue" --json labels --jq '.labels[].name' 2>/dev/null)

    echo "$labels" | grep -q "^${label}$"
}

# Check if issue is closed
gh_is_issue_closed() {
    local issue=$1

    local state=$(gh issue view "$issue" --json state --jq '.state' 2>/dev/null)

    if [ "$state" = "CLOSED" ]; then
        return 0  # True: issue is closed
    else
        return 1  # False: issue is open or error
    fi
}

# Get current branch
gh_get_current_branch() {
    git branch --show-current 2>/dev/null
}

# Check if on main branch
gh_is_on_main() {
    local branch=$(gh_get_current_branch)

    [[ "$branch" = "main" || "$branch" = "master" ]]
}

# Create feature branch for issue
gh_create_feature_branch() {
    local issue=$1
    local title=$2

    # Sanitize title for branch name
    local sanitized=$(echo "$title" | tr '[:upper:]' '[:lower:]' | sed 's/[^a-z0-9]/-/g' | sed 's/--*/-/g' | cut -c1-50)
    local branch_name="feature/${issue}-${sanitized}"

    # Always delete old branch if it exists (ensures fresh start from latest main)
    if git show-ref --verify --quiet "refs/heads/$branch_name"; then
        log_info "Deleting stale local branch: $branch_name"
        git branch -D "$branch_name" >/dev/null 2>&1 || {
            log_warning "Failed to delete old branch, will try to create anyway"
        }
    fi

    # Also delete remote branch if it exists (cleanup from previous failed attempts)
    if git ls-remote --heads origin "$branch_name" 2>/dev/null | grep -q "$branch_name"; then
        log_info "Deleting stale remote branch: $branch_name"
        git push origin --delete "$branch_name" >/dev/null 2>&1 || {
            log_warning "Failed to delete remote branch, continuing anyway"
        }
    fi

    # Always create fresh branch from current HEAD (which is now synced main)
    log_info "Creating fresh branch: $branch_name"
    git checkout -b "$branch_name" >/dev/null 2>&1

    if [ $? -eq 0 ]; then
        echo "$branch_name"
        return 0
    else
        log_error "Failed to create branch $branch_name"
        return 1
    fi
}

# Reset to main branch and sync with remote
gh_reset_to_main() {
    local main_branch="main"

    # Check if master exists
    if git show-ref --verify --quiet refs/heads/master; then
        main_branch="master"
    fi

    # Clean up untracked files and directories first to avoid conflicts
    git clean -fd >/dev/null 2>&1 || true

    # Force checkout to main branch (discarding any changes)
    git checkout -f "$main_branch" >/dev/null 2>&1 || {
        log_warning "Failed to checkout $main_branch, attempting recovery..."
        # If checkout fails, try harder reset
        git reset --hard HEAD >/dev/null 2>&1 || true
        git clean -fdx >/dev/null 2>&1 || true
        git checkout -f "$main_branch" >/dev/null 2>&1 || {
            log_error "Could not reset to $main_branch branch"
            return 1
        }
    }

    # Sync with remote main to get latest changes
    log_info "Syncing with remote $main_branch..."

    # Fetch latest from remote
    if git fetch origin "$main_branch" >/dev/null 2>&1; then
        # Reset local main to match remote (discarding any local commits)
        if git reset --hard "origin/$main_branch" >/dev/null 2>&1; then
            log_success "Synced with origin/$main_branch"
        else
            log_warning "Failed to sync with origin/$main_branch, using local $main_branch"
        fi
    else
        log_warning "Failed to fetch from origin, using local $main_branch"
    fi

    log_info "Reset to $main_branch branch"
}

