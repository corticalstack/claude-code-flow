# Request @claude PR review
gh_request_review() {
    local pr_number=$1

    # Mark PR as ready for review (required for draft PRs)
    log_info "Marking PR #$pr_number as ready for review..."
    if ! gh pr ready "$pr_number" 2>/dev/null; then
        log_warning "PR may already be ready for review"
    fi

    log_info "Requesting @claude review for PR #$pr_number..."
    gh pr comment "$pr_number" --body "@claude Please review this PR and submit an official GitHub review (not just a comment).

**You must explicitly APPROVE or REQUEST CHANGES using GitHub's review system.**

Review the implementation for:
- Code quality and best practices
- Security concerns
- Test coverage
- Adherence to plan specifications

Then submit your review decision:
- Click 'Approve' if the code is ready to merge
- Click 'Request changes' if issues must be fixed first

Do not just comment - submit an actual review with your decision." 2>/dev/null

    if [ $? -eq 0 ]; then
        log_success "Review requested from @claude"
        return 0
    else
        log_error "Failed to request review"
        return 1
    fi
}

# Check PR review status
gh_check_review_status() {
    local pr_number=$1
    
    # Get review decision (APPROVED, CHANGES_REQUESTED, REVIEW_REQUIRED, etc.)
    local review_decision=$(gh pr view "$pr_number" --json reviewDecision --jq '.reviewDecision' 2>/dev/null)
    
    echo "$review_decision"
}

# Get review comments
gh_get_review_comments() {
    local pr_number=$1

    gh pr view "$pr_number" --json comments --jq '.comments[] | select(.author.login == "claude-ai" or .author.login == "claude") | .body' 2>/dev/null
}

# Parse @claude's comment-based review decision
# Returns: APPROVED, CHANGES_REQUESTED, or empty string
gh_parse_claude_review_decision() {
    local pr_number=$1

    # Get all @claude comments (may be multiple)
    local claude_comments=$(gh pr view "$pr_number" --json comments --jq '.comments[] | select(.author.login == "claude") | .body' 2>/dev/null)

    if [ -z "$claude_comments" ]; then
        echo ""
        return
    fi

    # Check for approval indicators (simplified patterns, case insensitive)
    # Look for common approval phrases in @claude's reviews
    if echo "$claude_comments" | grep -Eiq "APPROVE|APPROVED|ready (to|for) merge|LGTM|looks good"; then
        # But exclude if it's asking about approval rather than giving it
        if ! echo "$claude_comments" | grep -Eiq "cannot.*approve|unable.*approve|can't.*approve"; then
            echo "APPROVED"
            return
        fi
    fi

    # Check for changes requested indicators
    if echo "$claude_comments" | grep -Eiq "CHANGES.{0,20}REQUESTED|REQUEST.{0,20}CHANGES|must.{0,20}fix|requires.{0,20}fix"; then
        echo "CHANGES_REQUESTED"
        return
    fi

    echo ""
}

# Auto-approve PR if @claude recommends approval
# This allows autonomous workflow to continue when @claude provides comment-based approval
gh_auto_approve_if_claude_approves() {
    local pr_number=$1

    local claude_decision=$(gh_parse_claude_review_decision "$pr_number")

    if [ "$claude_decision" = "APPROVED" ]; then
        log_info "@claude recommended approval in comment - auto-approving PR #$pr_number"

        if gh pr review "$pr_number" --approve --body "Auto-approved based on @claude's review recommendation." 2>/dev/null; then
            log_success "PR #$pr_number auto-approved"
            return 0
        else
            log_error "Failed to auto-approve PR #$pr_number"
            return 1
        fi
    fi

    return 1
}

# Merge PR
gh_merge_pr() {
    local pr_number=$1
    local issue=$2
    
    log_info "Merging PR #$pr_number..."
    
    if gh pr merge "$pr_number" --squash --delete-branch 2>&1 | tee "$(get_issue_log_dir $issue)/merge.log"; then
        log_success "PR #$pr_number merged successfully"
        return 0
    else
        log_error "Failed to merge PR #$pr_number"
        return 1
    fi
}

# Close issue
gh_close_issue() {
    local issue=$1
    local comment=$2
    
    if [ -n "$comment" ]; then
        gh issue comment "$issue" --body "$comment" 2>/dev/null
    fi
    
    gh issue close "$issue" 2>/dev/null
    
    if [ $? -eq 0 ]; then
        log_success "Issue #$issue closed"
        return 0
    else
        log_warning "Failed to close issue #$issue"
        return 1
    fi
}

# Build task queue from open issues
gh_build_task_queue() {
    local exempt_label="${RALPH_EXEMPT_LABEL:-ralph-exempt}"
    local issues=$(gh_fetch_open_issues 20)

    if [ $? -ne 0 ] || [ -z "$issues" ]; then
        echo "[]"
        return 0
    fi

    # Filter out issues with ralph-exempt label and build queue
    local queue=$(echo "$issues" | jq --arg exempt_label "$exempt_label" '[
        .[] |
        select(.labels | map(.name) | contains([$exempt_label]) | not) |
        {
            issue: ("#" + (.number | tostring)),
            title: .title,
            status: "pending",
            is_current: false,
            phases: {
                research: { status: "pending", attempts: 0 },
                planning: { status: "pending", attempts: 0 },
                implementation: { status: "pending", attempts: 0 },
                validation: { status: "pending", attempts: 0 }
            }
        }
    ]')

    echo "$queue"
}
